// BuildConfig holds the configuration for building an application artifact.
type BuildConfig struct {
	AppName     string         `json:"app_name"`
	Version     string         `json:"version"`
	Description string         `json:"description"`
	FlowIDs     []string       `json:"flow_ids,omitempty"`   // Specific flows to include (empty = all)
	Metadata    map[string]any `json:"metadata,omitempty"`
}

// Builder generates deployable application artifacts.
type Builder struct {
	relRepo *persistence.RelationalRepo
}

// NewBuilder creates a new artifact builder.
func NewBuilder(relRepo *persistence.RelationalRepo) *Builder {
	return &Builder{relRepo: relRepo}
}

// BuildArtifact generates a .tar archive containing the application snapshot.
func (b *Builder) BuildArtifact(ctx context.Context, config BuildConfig) (io.Reader, error) {
	var buf bytes.Buffer
	tw := tar.NewWriter(&buf)
	defer tw.Close()

	slog.Info("building artifact", "app", config.AppName, "version", config.Version)

	// 1. Generate Dockerfile
	dockerfile, err := b.generateDockerfile(config)
	if err != nil {
		return nil, fmt.Errorf("generate dockerfile: %w", err)
	}
	if err := addToTar(tw, "Dockerfile", dockerfile); err != nil {
		return nil, err
	}

	// 2. Generate docker-compose.yml
	compose, err := b.generateDockerCompose(config)
	if err != nil {
		return nil, fmt.Errorf("generate docker-compose: %w", err)
	}
	if err := addToTar(tw, "docker-compose.yml", compose); err != nil {
		return nil, err
	}

	// 3. Export node definitions
	nodes, _, err := b.relRepo.ListNodeDefinitions(ctx, 1000, 0)
	if err != nil {
		return nil, fmt.Errorf("list nodes: %w", err)
	}

	for _, node := range nodes {
		// Save node definition as JSON
		nodeJSON, _ := json.MarshalIndent(node, "", "  ")
		if err := addToTar(tw, fmt.Sprintf("nodes/%s.json", node.ID), string(nodeJSON)); err != nil {
			return nil, err
		}

		// Save script separately
		ext := "py"
		if node.ScriptLang == "nodejs" {
			ext = "js"
		}
		if err := addToTar(tw, fmt.Sprintf("scripts/%s.%s", node.ID, ext), node.ScriptContent); err != nil {
			return nil, err
		}
	}

	// 4. Export flow definitions
	flows, _, err := b.relRepo.ListFlowGraphs(ctx, 1000, 0)
	if err != nil {
		return nil, fmt.Errorf("list flows: %w", err)
	}

	for _, flow := range flows {
		flowJSON, _ := json.MarshalIndent(flow, "", "  ")
		if err := addToTar(tw, fmt.Sprintf("flows/%s.json", flow.ID), string(flowJSON)); err != nil {
			return nil, err
		}
	}

	// 5. Generate SQL schema
	schema := generateSchema()
	if err := addToTar(tw, "schema.sql", schema); err != nil {
		return nil, err
	}

	// 6. Build metadata
	meta, _ := json.MarshalIndent(map[string]any{
		"app_name":    config.AppName,
		"version":     config.Version,
		"description": config.Description,
		"built_at":    time.Now().UTC().Format(time.RFC3339),
		"nodes_count": len(nodes),
		"flows_count": len(flows),
		"metadata":    config.Metadata,
	}, "", "  ")
	if err := addToTar(tw, "manifest.json", string(meta)); err != nil {
		return nil, err
	}

	slog.Info("artifact built successfully",
		"nodes", len(nodes),
		"flows", len(flows),
	)

	return &buf, nil
}

// generateDockerfile creates an optimized Dockerfile for production.
func (b *Builder) generateDockerfile(config BuildConfig) (string, error) {
	tmpl := template.Must(template.New("dockerfile").Parse(dockerfileTmpl))
	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, config); err != nil {
		return "", err
	}
	return buf.String(), nil
}

// generateDockerCompose creates a docker-compose.yml for the application.
func (b *Builder) generateDockerCompose(config BuildConfig) (string, error) {
	tmpl := template.Must(template.New("compose").Parse(composeTmpl))
	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, config); err != nil {
		return "", err
	}
	return buf.String(), nil
}

func addToTar(tw *tar.Writer, name, content string) error {
	data := []byte(content)
	hdr := &tar.Header{
		Name:    name,
		Mode:    0644,
		Size:    int64(len(data)),
		ModTime: time.Now(),
	}
	if err := tw.WriteHeader(hdr); err != nil {
		return fmt.Errorf("tar write header %s: %w", name, err)
	}
	if _, err := tw.Write(data); err != nil {
		return fmt.Errorf("tar write content %s: %w", name, err)
	}
	return nil
}

func generateSchema() string {
	return `-- Genoma Application Schema
-- Auto-generated by Genoma Builder

CREATE EXTENSION IF NOT EXISTS "uuid-ossp";
CREATE EXTENSION IF NOT EXISTS vector;

CREATE TABLE entity_refs (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    entity_type VARCHAR(100) NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE audit_logs (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    entity_type VARCHAR(100) NOT NULL,
    entity_id UUID NOT NULL,
    action VARCHAR(50) NOT NULL,
    actor VARCHAR(200),
    metadata JSONB,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE document_store (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    entity_ref_id UUID NOT NULL REFERENCES entity_refs(id) ON DELETE CASCADE,
    schema_version INT NOT NULL DEFAULT 1,
    data JSONB NOT NULL DEFAULT '{}',
    metadata JSONB,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE vector_store (
    id UUID PRIMARY KEY DEFAULT uuid_generate_v4(),
    entity_ref_id UUID REFERENCES entity_refs(id) ON DELETE CASCADE,
    content_type VARCHAR(50) NOT NULL,
    content_text TEXT NOT NULL,
    embedding vector(384) NOT NULL,
    metadata JSONB,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_doc_data ON document_store USING GIN(data);
CREATE INDEX idx_vector_embedding ON vector_store USING ivfflat (embedding vector_cosine_ops) WITH (lists = 100);
`
}

// --- Templates ---

const dockerfileTmpl = `# Genoma Application: {{.AppName}}
# Version: {{.Version}}
# Auto-generated by Genoma Builder

FROM golang:1.22-alpine AS builder
WORKDIR /build
COPY . .
RUN CGO_ENABLED=0 go build -o /app/genoma ./cmd/genoma

FROM alpine:3.19
RUN apk --no-cache add ca-certificates docker-cli
WORKDIR /app
COPY --from=builder /app/genoma .
COPY nodes/ /app/nodes/
COPY flows/ /app/flows/
COPY scripts/ /app/scripts/
COPY schema.sql /app/schema.sql

EXPOSE 8080
HEALTHCHECK --interval=30s --timeout=5s CMD wget -qO- http://localhost:8080/health || exit 1
USER 65534
ENTRYPOINT ["./genoma"]
`

